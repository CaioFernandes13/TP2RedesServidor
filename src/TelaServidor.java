    private void btnOffOnServidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOffOnServidorActionPerformed
        // TODO add your handling code here:
        int porta = Integer.parseInt(tfPorta.getText());
        if(server.isFechado()){
            if(server.ligaServidor(porta)){
                lblStatusServidor.setText("Ligado com 0/2 conexoes");
                try {
                    lblInfo.setText(InetAddress.getLocalHost().getHostAddress());
                    lblInfo.setForeground(Color.blue);
                } catch (UnknownHostException ex) {
                    Logger.getLogger(TelaServidor.class.getName()).log(Level.SEVERE, null, ex);
                }
                tfPorta.setVisible(false);
                lblPorta.setVisible(false);
                btnOffOnServidor.setText("Desconectar");
            }
            else{
                lblInfo.setText("Erro ao ligar servidor!");
                lblInfo.setForeground(Color.red);
            }
        }
        else{
            server.desligaServidor();
            lblInfo.setText("");
            lblStatusServidor.setText("Offline");
            tfPorta.setVisible(true);
            lblPorta.setVisible(true);
            btnOffOnServidor.setText("Conectar");
        }
    }//GEN-LAST:event_btnOffOnServidorActionPerformed

